    public class IndustryLatestNewsStructuredQuery : AbstractIndustryStructuredQuery
    {
        private static readonly string[] DEFAULT_NEWS_SUBJECTS =
            new[] {"NFCPIN"};

        private static readonly string[] default_RESTRICTORS =
            new[] {"tmnb", "tusn", "tukn", "teun", "taun", "tbnw", "tfcp"};

        private static readonly string[] EXCLUDED_NEWS_SUBJECTS =
            new[] {"NRGN", "NIWE", "NRVW"};

        private static readonly string[] EXCLUDED_SOURCES =
            new[] {"rns", "datmon", "jobpub"};


        private string editorChoiceCode;
        private IndustryQueryInfo mapping;

        protected override void BuildSearchStringList()
        {
            mapping = IndustryQueryUtility.GetMapping(Industry.Fcode);
            if (mapping == null)
            {
                var searchString = GetIndustryFilter(new[] {Industry.Fcode});
                ListSearchString.Add(searchString);
                return;
            }
            editorChoiceCode = IndustryEditorChoiceStructuredQuery.GetIndustryNewsSubject(Industry.Fcode);
            if (editorChoiceCode != null)
            {
                BuildEditorChoiceTypeQuery();

                ExcludeSourcesAndSubjects();
            }
            else
            {
                var searchString = GetIndustryFilter(new[] {Industry.Fcode});
                ListSearchString.Add(searchString);

                searchString = GetNewsSubjectFilter(DEFAULT_NEWS_SUBJECTS);
                ListSearchString.Add(searchString);

                InsertMappingSearchString();

                ExcludeSourcesAndSubjects();
            }
        }

        private void ExcludeSourcesAndSubjects()
        {
            var searchString = GetExcludeSearchString("sc", EXCLUDED_SOURCES);
            ListSearchString.Add(searchString);
            searchString = GetExcludeSearchString("ns", EXCLUDED_NEWS_SUBJECTS);
            ListSearchString.Add(searchString);
        }


        private void InsertMappingSearchString()
        {
            var mappping = IndustryQueryUtility.GetMapping(Industry.Fcode);
            var restrictors = new List<string>();
            if (mappping != null)
            {
                restrictors.AddRange(new List<string>(mappping.Restrictors));
            }
            restrictors.AddRange(new List<string>(default_RESTRICTORS));
            var searchString = GetRestrictorFilter(restrictors.ToArray());
            ListSearchString.Add(searchString);

            if (mappping == null || mappping.WordCount <= 0)
                return;
            searchString = new SearchString
                               {
                                   Id = "LegacyPart", 
                                   Type = SearchType.Free, 
                                   Value = "wc>" + mappping.WordCount, 
                                   Mode = SearchMode.Traditional
                               };

            ListSearchString.Add(searchString);
        }

        private void BuildEditorChoiceTypeQuery()
        {
            var mappping = IndustryQueryUtility.GetMapping(Industry.Fcode);

            var sb = new StringBuilder();
            sb.Append("(");
            sb.Append("in=").Append(Industry.Fcode);
            sb.Append(" and ").Append("ns=").Append(DEFAULT_NEWS_SUBJECTS[0]);
            sb.Append(" and rst=(").Append(String.Join(" or ", default_RESTRICTORS));
            sb.Append(" or ").Append(String.Join(" or ", mappping.Restrictors));
            sb.Append(")");
            sb.Append(" and wc>").Append(mappping.WordCount);
            sb.Append(")");
            sb.Append(" or ");
            sb.Append("ns=").Append(editorChoiceCode);

            var searchString = new SearchString
                                   {
                                       Id = "LegacyPart", 
                                       Type = SearchType.Free, 
                                       Value = sb.ToString(), 
                                       Mode = SearchMode.Traditional
                                   };
            ListSearchString.Add(searchString);
        }


        protected override Dates GetDefaultDateOption()
        {
            var dates = base.GetDefaultDateOption();
            dates.After = DATE_RANGE_LAST_3MONTHS;
            return dates;
        }
    }

